public class Calc {
    public static int calc(int a, int b, char ma) {
        int sum = 0;
        switch (ma){
            case '+':
                sum = a + b;
                break;
            case '-':
                sum = a - b;
                break;
            case '*':
                sum = a * b;
                break;
            case '/':
                sum = a / b;
                break;}
                return sum;




        }


    }

